//validation phần dữ liệu nhập vào
function OnDangky(event) {
  if (checkID($("#mssv").val()) == false) {
    $("#mssv").css("color", "red");
  } else {
    $("#mssv").css("color", "black");
  }
  if (checkName($("#hoten").val()) == false) {
    $("#hoten").css("color", "red");
  } else {
    $("#hoten").css("color", "black");
  }
  if (checkDC($("#diachi").val()) == false) {
    $("#diachi").css("color", "red");
  } else {
    $("#diachi").css("color", "black");
  }
  if (checkPhonenumber($("#dienthoai").val()) == false) {
    $("#dienthoai").css("color", "red");
  } else {
    $("#dienthoai").css("color", "black");
  }
  if (checkEmail($("#email").val()) == false) {
    $("#email").css("color", "red");
  } else {
    $("#email").css("color", "black");
  }
}
function checkID(str) {
  let regex1 = /^\d{8}$/;
  var regex2 = str.localeCompare("16") == 1 && str.localeCompare("23") == -1;
  return regex1.test(str) && regex2;
}
function checkName(str) {
  let regex1 = /^[A-Z].{1,}(\s[A-Z].{1,}){1,}$/;
  return regex1.test(str);
}
function checkDC(str) {
  let regex1 = /^\w.{1,}(\s\w.{1,}){1,}$/;
  return regex1.test(str);
}
function checkEmail(str) {
  let regex = /^[a-z][a-z0-9_\.]{5,29}@[a-z0-9]{2,}(\.[a-z0-9]{2,}){1,3}$/;
  return regex.test(str);
}
function checkPhonenumber(str) {
  let regex = /^[0]\d{9}$/;
  return regex.test(str);
}
//đăng ký 1 nhóm môn
function dkmonhoc(e) {
  var dsmon = document.getElementsByClassName("dsmonactive");
  var them = document.getElementById("uldsmondc");
  i=0;
  while(dsmon.length!=0)
  {
    var p = document.createElement("li");
    p.interHTML = '<li class="dsmondc">' + dsmon[i].innerText + "</li>";
    p.innerText = dsmon[i].innerText;
    p.className = "dsmondc";
    p.addEventListener("click", function () {
      this.classList.toggle("dsmondcactive");
    });
    dsmon[i].hidden = true;
    dsmon[i].className = null;
    them.appendChild(p);
  }
}
